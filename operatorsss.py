"""
operators in python:

arithmatic operators
assignment operators
comparison operators
logical operators
identity operators
membership operators
bitwise operators
"""
# arithmatic operator
print("8 * 9 is", 8 * 9)
print("2**3 is", 2**3)
print("10/9 is", 10/9)
print("5%3 is", 5%3) # modulus operator
print("10//9 is", 10//9) # floar division operator

# assignment operators
print("assignment operators")
x=5
print(x)
x +=3
print(x)

# comparison operators
print("comparison operators")
i=5
print(i==8)
print(i>=4)

# logical operators
print("logical operators")
a=True
b=False

print(a and b)
print(a or b)

# identity operators
print("identity operators")
print(5 is 7)
print(5 is not 7)
print(5 is 5)

# membership operators
print("membership operators")
list = [1,5,9,25,45,56,1]
print(455 in list)

# bitwise operators
print("bitwise operators")
print(0 & 1)
print(0 | 3)